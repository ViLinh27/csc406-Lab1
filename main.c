/*
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1,
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
2 *   Rating: 3
 */
int addOK(int x, int y) {
    int mask = ~(1<<31);//moving 1 left 31 bits makes the entire binary word a series of 1s. This makes a mask to cover the entire 32 bits of a binary word.
  return !((((x>>1)& mask)+((y>>1)& mask)) >> 31);
  //(x>>1) & mask ensures that we get at least 1 bit outside the memory bounds (x>>1) and that we've isolated the appropriate 32 bits (& mask).
  //The ! booean operator looks for a 0 value in the (x>>1) & 1 part of the return to make sure
  // The second half with (y>>1) & mask works the same way as mentioned above.
  //The >>31 shifts the resulting boolean to the least significant position for the output(boolean).
}
/*
 * isLessOrEqual - if x <= y  then return 1, else return 0//////////////return 1 if x<y | x==y
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */

 int isLessOrEqual(int x, int y) {//y-x>=0
     //y+(~x)+1>=0
     //two cases:
     //x is negative, y is positive
     //x and y have the same sign
     int xisPositive = !((x>>31)&1);//what if x is positive
     int yisPositive = !((y>>31)&1);//what if y is positive
     int xLEyNoOverflow = !(1 & ((y+(~x)+1)>>31));//checks overflow (esecially for extreme values)
  return (((!xisPositive)& yisPositive) | xLEyNoOverflow) & (!(xisPositive & (!yisPositive)));
}
/*
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  return (~(x>>31)&x)|((x>>31)&(~x+1));
  //(x>>31)&(~x+1) covers the possibility of negative x values.
}



